document.getElementById('sel_row_level19').addEventListener('change', function(e) {
    document.querySelector('#classid19').destroy();
    VirtualSelect.init({
      ele: "#classid19",
        multiple: true,
        search:false,
        textDirection: 'rtl',
        disableSelectAll: true,
        disabledOptions:[1]
    });
    let value =this.value;
    let dataUrl = '<?php echo site_url();?>' + '/emp/class_table/getclass_based_student/'+value;
    console.log(dataUrl)
    if(e.target.value.length > 0){
        fetch(dataUrl)
        .then(response => {
            if (!response.ok) {
                throw new Error(`HTTP error! Status: ${response.status}`);
            }
            return response.json();
        })
        .then(data => {
             data.GetClass.forEach(select => {
                document.querySelector('#classid19').addOption({
                    value: select.ClassID,
                    label: select.LevelName + '--' +  select.RowName + '--' + select.ClassName
                });
            });
            
        })
        .catch(error => {
            console.error('Error:', error);
        });
    }
});

